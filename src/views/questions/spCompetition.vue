 <p class="title">网球规则：</p>

 <p class="title">网球简介：</p>
<p class="con">网球运动是一种球类运动，通常在两个单打球员或两对组合之间进行。</p>
<p class="con">球员在网球场上隔着球网用网球拍击打网球。</p>
<p class="con">网球运动的由来和发展可以用四句话来概括：孕育在法国，诞生在英国，</p>
<p class="con">开始普及和形成高潮在美国，现在盛行全世界，被称为世界第二大球类运动。</p>

<p class="con">戴维斯杯所有比赛、奥运会单双打决赛、四大满贯单打比赛、温网双打比赛
是五盘三胜制，其余比赛均为三盘两胜制。(2011年温网男双首轮改为三盘两胜制，其他轮次依然为五盘三胜制)</p>

 <p class="title">网球如何投注：</p>
<p class="con">打开体育投注页面，找到自己感兴趣的赛事玩法，可以看到每个玩法开出的赔率，
您点击赔率，在投注额中输入您的投注金额后点击【投注】，显示该注单：您的注单已被确认，
您点击确认，那么这个注单就投注成功了。</p>

<p class="con">网球小提示☞投注的时候注意区分让盘和让局的区别哦；右下角会有显示总盘数。</p>

 <p class="title">网球基本规则：</p>
<p class="con">戴维斯杯所有比赛、奥运会单双打决赛、四大满贯单打比赛、温网双打比赛是五盘三胜制，
其余比赛均为三盘两胜制。(2011年温网男双首轮改为三盘两胜制，其他轮次依然为五盘三胜制)</p>

 <p class="title">网球盘的规则和组成：</p>
<p class="con">网球比赛中。率先获得6局并领先对手至少2局的选手赢得一盘。
比赛。若其中一方赢得6局，但并未领先2局，则该盘继续，直到领先2局，也就是说必须
达到 6:4 ；7:5；8:6。但是如果比分是6:5； 7:6； 8:7等等，则该盘继续，因为没有领
先至少2局，也有一些比赛在双方战成6:6时，进入决赛局，看谁先赢得7局，则胜利，也就
是抢七局。也有些按照传统方式一直战到领先对手2局为止。</p>

 <p class="title">网球局的规则和组成：</p>
<p class="con">网球一局：</p>

<p class="con">①一局比赛只有一名选手发球，率先赢得至少4分并且多出对手至少2分的选手赢得一局；</p>
<p class="con">②网球每局记分方式为：从0至3分，分别记为：、 零、十五、 三十、四十，计分时，发球手的得分在前
因此30:0的他意思是，发球手赢得2分，而接球手还未得分。</p>
<p class="con">③当双方选手都得3分时，一般叫做“平局”而不是说 40:40。在出现平局后一名选手再得一分，被称之为
“占先”。而不再计分， 如果占先后再得一分，就赢得一局了。</p>
<p class="con">④发球手先占或者在领先对手2分的情况下，被称作握有“局点”，如果是赢得这局后又恰好可以赢得一盘，
就可以称作是“盘点”， 能赢得比赛就称之为“赛点”。</p>

 <p class="title">网球局的规则和组成：</p>
<p class="con">网球一局：</p>

<p class="con">①一局比赛只有一名选手发球，率先赢得至少4分并且多出对手至少2分的选手赢得一局；</p>
