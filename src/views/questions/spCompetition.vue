 <p class="title">网球经常中断：</p>
<p class="con">网球经常中断的原因：</p>

<p class="con">1：因为网球是没有任何时间限制的，需要打满赛制规定的盘和局才可以结束，有时候一盘几个小时下来运动员身体是吃不消的，就需要暂停中断一会儿才可以继续比赛。</p>
<p class="con">2：而且网球队场地要求也较高。如果是草地长时间赛制中途更换也是会显示中断暂停的。</p>
<p class="con">目前为止网球比赛时间最长的记录是：牛津郡的网球教练安迪阿特肯和莱恩特雷基连续打网球72小时，两人击打了接近5000分。</p>

 <p class="title">网球比赛时间：</p>

<p class="con">网球是没有比赛时间限制的，网球必须是要打完规定
的盘数和局数才可以结束分出输赢的呢。
所以有时候一场网球比赛好几天也是有可能的哦。
目前为止网球比赛时间最长的记录是：牛津郡的网球教练安
迪阿特肯和莱恩特雷基连续打网球72小时，
两人击打了接近5000分。</p>

