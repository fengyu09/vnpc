 <p class="title">足球红黄牌：</p>

<p class="con">如果有球员在比赛期间犯规，裁判会给出一张黄牌，被黄牌警告的球员仍可继续比赛，
但当接受第二次警告时便会被逐离场，按程序裁判会先出示第二面黄牌，再出示红牌，
有就是两黄变一红，被罚球员将不能继续进行余下的赛事。球队也不能用后备球员补上，
球队需在缺人下继续比赛。</p>

 <p class="title">还有绿牌哦：</p>
<p class="con">在2007年，绿牌被引入了足球比赛中，不过最早是在青少年联赛中才使用的，如果某位
球员在球赛中表现出了公平、友好的高尚体育道德，那么裁判就会对他出示绿牌，以表
彰他的高尚行为。其实简单说来就是，为了表彰球员在比赛中良好的职业道德和高尚行
为，裁判才会对他出示绿牌。</p>

<p class="con">1.在单双玩法中，如无红、黄牌出现，即“0”，此局可视为平局。</p>
<p class="con">2.在实际投注中，一个黄牌为1分，一个红牌为2分。(单场赛事每个球员最高可计3分)</p>
<p class="con">3.针对非球员出示的任何罚牌将不计算在内。</p>

 <p class="title">足球加时赛：</p>
 <p class="title">什么时候会开始加时赛呢：</p>

<p class="con">一定要分出胜负的赛事，若在90分钟+伤停补时后还是平局，会继续进
行30分钟的加时赛，上下半场15分钟，没有伤停补时时间，中间原则上
没有休息时间,但一般裁判都会给几分钟让球员接受按摩或者喝水，然后
交换场地进行下半时。如果30分钟后没有分出胜负，则直接进行点球大战。
常规足球赛事不包含加时赛。</p>

 <p class="title">足球净胜球：</p>
<p class="con">按照一场赛事90分钟+伤停补时内预测完场比赛结束后的净胜球数。
投注净胜球最后必须是刚好净胜对方投注的数量才可以赢，
比如投注，尤文图斯vs曼城 尤文图斯净胜球2，那么就必须是完场比分
尤文图斯刚好比曼城多2个球才可以盈利。其他都是输掉的。</p>

 <p class="title">足球零失球：</p>
<p class="con">1.预测某个球队在90分钟的比赛中，没有任何失球。</p>
<p class="con">2.选择投注球队不需要赢得比赛，例如：赛果 0-0，注单为赢。</p>
<p class="title">IM体育零失球规则为：</p>
<p class="con">规则1：</p>
<p class="con">主队得分≥ 1, 客队得分= 0 (1, 2…: 0)
主队「没有失球」– 退款 客队「没有失球」 – 输
主队「有失球」– 退款 客队「有失球」– 赢</p>
<p class="con">规则2：</p>
<p class="con">主队得分≥ 1, 客队得分≥ 1 (1, 2…: 1, 2…)
主队「没有失球」– 输    客队「没有失球」– 输
主队「有失球」– 赢   客队「有失球」– 赢</p>
<p class="con">规则3：</p>
<p class="con">主队得分= 0, 客队得分≥ 1 (0 : 1, 2…)
主队「没有失球」– 输 客队「没有失球」 – 退款
主队「有失球」– 赢 客队「有失球」– 退款</p>

 <p class="title">足球点球大战：</p>
<p class="con">点球大战开始前双方各自先确定好本队罚点球的队员和出场顺序，
通常通过猜硬币的方式决定由哪一方先罚。比赛开始后，双方轮流罚球，共罚
5轮，5轮结束之后以累计进球数多的一方获胜。如果5轮罚球结束双方仍未分
出胜负，则采取“突然死亡法”进行加罚：即双方继续互罚，直到出现某一轮
结束时一方罚进而另一方未罚进的局面，则由罚进的一方取得胜利。</p>

<p class="con">如果您有投注点球大战的赛事，最后没有进行点球大战，那么是会退本金的哦。
足球点球大战只有让球盘口会计算最后一颗球【突然死亡】。其他的盘口都是
只计算前五轮十个球， 不计算最后一颗球。而且让球盘口也是要减去即时比分的。</p>

 <p class="title">足球首个进球：</p>
<p class="con">在指定时间里，可以是完成也可以是规定时间里以指定方式进球的第一个球，可以分为三种哦：
</p>
 <p class="title">足球首个进球方式：</p>
<p class="con">足球预测首个进球的方式。</p>

<p class="con">如果赛事在首个进球后中断，所有首个进球方式的注单将保持有效。</p>
<p class="con">选项可列为：</p>
<p class="con">A.任意球：球必须是直接踢进的方式。间接行的任意球如果最后是罚球者本人踢进则计算在内。</p>
<p class="con">B.点球：球必须是由罚球者本人直接踢进的方式。若遇到补射的情况，即使是罚球者本人踢进也将不计算在内。</p>
<p class="con">C.乌龙球：球必须授予为乌龙球。
<p class="con">D.头球：进球者必须明确的用头进球。</p>
<p class="con">E.射门：所有其他的进球方式。除了以上所列出的方式，所有其他进球的方式都包含在此方式。</p>
<p class="con">F.没有进球：赛事没有进球。</p>

 <p class="title">足球首个进球时间：</p>
<p class="con">①.预测在90分钟完场时间内首个进球时间。</p>
<p class="con">IM体育进球时间为00:01 – 15:00 ~~15:01 – 30:00 以此类推</p>
<p class="con">③.如果赛事在首个进球后中断，所有首个进球时间的注单将保持有效。</p>
<p class="con">④.如果赛事在没有进球前中断，所有首个进球时间的注单将被取消。</p>
<p class="con">⑤.乌龙球将予以计算在内。裁判判定无效的进球是将不予以计算在内。</p>

 <p class="title">足球首个进球时间-3项：</p>
<p class="con">1.预测在90分钟完场时间内首个进球时间，盘口提供无进球投注选项。</p>
<p class="con">2.选项可列为：</p>
<p class="con">选项1 小于等于26分钟</p>
<p class="con">选项 2 大于或等于27分钟</p>
<p class="con">选项 3 没有进球</p>
<p class="con">3.出于结算的用意，赛事的第一分钟是从1秒计算到59秒。第二分钟则是从1分钟计算到1分59秒，以此类推。</p>
<p class="con">4.范例：如果投注首个进球时间的选项是‘赛事的第26分钟或之前’，而确实进球的时间为26分钟49秒，
进球的范围属于‘第27分钟后’，因此投注将结算为输。</p>
<p class="con">5.如果赛事在首个进球后中断，所有首个进球时间的注单将保持有效。</p>
<p class="con">6.如果赛事在没有进球前中断，所有首个进球时间的注单将被取消。</p>
<p class="con">7.乌龙球将予以计算在内。裁判判定无效的进球是将不予以计算在内。</p>

 <p class="title">足球双重机会：</p>
<p class="con">足球双重机会：简化条件的标准盘玩法 也叫 双胜彩 </p>

<p class="con">就是一个选项包括两种赛果，任选一个选项符合赛果就算盈利。</p>
<p class="con">任意猜中一个就赢(假如投注“主或客”，那么主队胜利或者是客队胜利都是盈利的，和局就输)</p>

 <p class="title">足球三项让分：</p>
<p class="con">足球三项让分又称为标准盘让球 特殊标准盘玩法 ，</p>
<p class="con">赛果首先要抵消让分条件，剩余赛果再判断标准盘胜负平。</p>
<p class="con">小助手给您举例说明哦：</p>

<p class="con">1.主(-1)投注的队伍以2球或以上队伍胜出</p>
<p class="con">2.和(-1)投注的队伍1球净胜胜出</p>
<p class="con">3.客(+1)客队投注的队伍胜出或者平局盈利</p>

 <p class="title">足球奇幻赛事：</p>
<p class="con">梦幻赛也是奇幻赛。就是由两个现实中不会遇到的两个球队进行假想踢球。</p>
<p class="con">按照各自实际进球数，进球时间，攻击指数和防守指数，进行对比。篮球和足球一样的玩法。</p>

 <p class="title">做两个举例：</p>
<p class="con">案例1：梦幻赛：张飞和李逵要是战场遇到谁胜谁负。按照战斗指数和智力指数。张飞是完胜的。</p>
<p class="con">案例2：【上海申花vs 广州恒大】最后比分为4-2；【 皇家马德里vs热刺】 最后比分为3-1； </p>
<p class="con">梦幻赛就可以开出 【上海申花vs热刺】，最后他们的进球比分就是4-1、买上海申花的盈
利。热刺的输。</p>

 <p class="title">足球双半场进球：</p>
<p class="con">1.乌龙球将只会被计算在得分球队一方；</p>
<p class="con">2.如果投注球队只有在一个半场有入球，或未入球，注单为输；</p>
<p class="con">3.预测主队/客队在90分钟完场时间内是否在上下半场都至少进一球；</p>
<p class="con">4.如果赛事在所投注的球队已在两个半场都进球后中断，所有投注此球队的注单将结算为赢；</p>
<p class="con">5.如果上半场进球球队已经确认，而赛事在下半场中断，那么所有投注该球队注单将被视为有效；</p>
<p class="con">6.如果赛事在下半场中断，并且所投注的球队并未在上半场进球，所有投注此球队的注单将结算为输。</p>

 <p class="title">足球位置及独赢：</p>
<p class="con">在我们的玩法里面有一个【位置及独赢】 的投注选项。这个玩法是由两个独立的投注注单组合的一个选项，</p>
<p class="con">举个例子给您，您就瞬间明白了：</p>

<p class="con">假如下注金额10元。赔率为8 。下注1/2的赔率。</p>
<p class="con">1：独赢计算：本金*赔率-本金→10*8=80-10=70元:；</p>
<p class="con">2：位置计算：本金*【(赔率-1)÷几分+1】-本金→10X【(8-1)÷2+1】-10=35元</p>
<p class="con">那么位置及独赢的盈利金额就是 70+35=105元。</p>

 <p class="title">足球赢得任一半场：</p>
<p class="con">1.预测选择的球队在90分钟完场时间内(不包括加时赛及点球赛)是否在上/下半场的其中一个半场进球数多于对手；</p>
<p class="con">2.如果赛事在下半场中断，但在上半场其中一方球队已经获胜，注单将保持有效。如果两支球队在上半场平局，注单将被取消；</p>
<p class="con">3.如果赛事出现平局或上下半场均无进球，所有注单将视为输。如双方球队各赢半场，则投注两个球队注单为赢。</p>

 <p class="title">足球赢得所有半场：</p>
<p class="con">1.预测选择的球队在90分钟完场时间内(不包括加时赛及点球赛)是否在上半场和下半场的进球数多于对手。</p>
<p class="con">2.如果赛事中断，所有注单将被取消；</p>
<p class="con">3.如果任何一个半场或上/下半场的结果是平局或没有进球，所有注单将结算为输。</p>

 <p class="title">足球双方球队进球：</p>
<p class="con">1.预测在指定时间【上半场、下半场】里是否有进球；</p>
<p class="con">2.如果赛事在上半场双方球队都有进球后中断，所有注单保持有效。</p>
<p class="con">3.如果赛事在上半场双方球队没有进球前中断或延迟，所有注单将被取消。</p>
<p class="con">4.乌龙球将予以计算为得分那方入球。</p>

 <p class="title">足球15分钟进球时间：</p>
<p class="con">预测在指定时间内是否有进球。15分钟时间段是指：</p>
<p class="con">00:01 – 15:00，第二个15:01 – 30:00 以此类推。</p>

 <p class="title">足球首粒入球/最后一粒入球：</p>
<p class="con">在规定时间内【若无特殊说明指90分钟+伤停补时时间内】猜测第一颗是哪支队伍踢进的，
以及最后最后一颗球是哪支队伍踢进。</p>
<p class="con">还可以投注：没有进球，没有进球就是猜测在该时间段两支队伍都没有进球。

</p>
 <p class="title">足球赔率变化的原因：</p>
<p class="con">足球为什么赔率会一直变化呢。特别是滚球盘口，引起赔率变化主要有两个原因：</p>

 <p class="title">足球场上有人进球了：</p>
<p class="con">①.上半场结束后，双方还是没进球，那么和局的水位就会降低，单双中的【双】玩法赔率也会降低，
波胆里面的【0-0】、【1-1】、【2-2】赔率也一样会降低。如果双方实力不分高下，那么主队的赔率一
定会比客队的赔率低一些，因为主队占了主场优势。主客队的赔率变化，要看当时哪一队攻势比较
猛烈，来进行调整；</p>
<p class="con">②.如果是让球的话，比如开赛时主让2球赔率2.04，客队为1.79.开出这个赔率可以看到主队
是绝对的强队，但是如果赛事开始以后一直到下半场主队一直没有超过客队2个球，那么这
个时候一般有两个变化哦：一个是主队让2球的水位赔率会上调，客队的水位赔率会下调。另外
一个变化可能会出现升盘或者降盘的情况。比如说之前是让2球，会变成主让1.5/2球。还是
不行继续下降到1/1.5，一直下降到一个合理的范围停止。</p>

<p class="con">注：足球一次上升或者下降一个盘为0.25，篮球一次为0.5。所以只有足球会出现赢半或输半，而篮球没有赢半输半的情况。</p>

 <p class="title">足球该玩法增加了投注量：</p>
<p class="con">这个盘口的玩法有大量的注单投注到这个盘口：</p>

<p class="con">我们一般会维持上盘和下盘的投注金额等量，如果有不等量的注单金额进来，</p>
<p class="con">我们就会通过水位去调整整个盘口的水位。</p>
<p class="con">有点像资本主义市场的杠杆原理，举例说明：</p>
<p class="con">比如说上盘赔率为3，投注注单为300元。这个时候上盘总量为3X300=900元。这个是一个基数。
<p class="con">那这个时候下盘赔率为1.5，我们就需要有投注注单为900除以1.5=600，也就是说我们这个时候
需要有下盘投注注单为600元才能保持平衡，如果不平衡的话，我们将会调整水位赔率，
以使上下两个盘的总量达到一个平衡状态。</p>

 <p class="title">足球五大联赛浅度解析：</p>
<p class="con">当今足坛最具观赏性以及投注趣味于一身的五大联赛</p>

 <p class="title">英超：</p>
<p class="con">1.全名：英格兰足球超级联赛</p>
<p class="con">2.球风：英超 比赛场面激烈、注重身体对抗、比赛节奏快、现在英超是全世界最受观影的联赛，也是收入最高的联赛。</p>
<p class="con">3.历史：成立于1992年2月20日，其前身是英格兰甲级联赛。历史上夺冠次数最多的是曼联。英超21世纪呈现曼联，
曼城，托特纳姆热刺，切尔西，阿森纳和利物浦六强鼎立的局面。</p>
<p class="con">4.晋级及降级：英超成立后至今(1992～2020)，共有6支球队没有降级过，他们分别是：曼联、埃弗顿、切尔西、
阿森纳、利物浦和托特纳姆热刺。此外，1919年(英国甲级)联赛扩军，阿森纳升入顶级联赛后至今(截止到20
15-2020赛季，已经走过了101个赛季)，再没降过级，但在此之前曾于1913年降入乙级联赛。但由于当时的英甲
并未正式扩军，所以一般还是认为阿森纳并未从顶级联赛降级。</p>

 <p class="title">意甲：
<p class="con">1.全名：意大利甲级足球联赛</p>
<p class="con">2.球风：意甲一向是世界上水平最高的职业足球联赛之一，其特点为注重防守。</p>
<p class="con">3.历史：意大利足球甲级联赛初始于1898年，上世纪八、九十年代，意甲球星云集，
实力强劲的球队众多，一度被公认为是世界第一足球联赛，被誉为“小世界杯”。
但进入21世纪后，多家俱乐部财政问题爆发，各种因素作用下，意甲在欧洲的地位呈下滑趋势。</p>
<p class="con">4.晋级及降级：比赛制度为双循环制，每次联赛结束后的最后三名将被淘汰进入乙级联赛。尤文图
斯是历史上夺得最多联赛冠军的球队，一共夺得过36次，其次为AC米兰(18次)、国际米兰18次
)及热那亚(9次)。每夺得十个联赛冠军可以在队徽绣上一颗金星，因此尤文图斯拥有3颗金星，
而国际米兰及AC米兰各有1颗。但在2006年“电话门”曝光后，意甲水平大幅下降。意甲的技术
特点为注重防守，但2010年来意甲的进球率有所增加。早先意甲呈现七强争王局面，近几年冠军
为北方三强垄断(尤文图斯、国际米兰、AC米兰)。不过其中2006年电话门事件后，尤文图斯降入乙
级联赛，但是仅仅6年后，他们又重新站到意甲最高的领奖台上。</p>

