<p class="con">危险球小贴士：无条件危险球有点球、单刀球、红牌；有条件危险球有任意球、角球、射门、危险进攻。</p>

 <p class="title">足球锁盘不让投注：</p>
<p class="con">亲亲投注的盘口锁定中一般都是以下三个原因造成的呢：</p>
<p class="con">1.现场出现了危险球；</p>
<p class="con">2.卫星信号中断；</p>
<p class="con">3.现场有变故，比如说球场进球，停电等等也是会锁定盘口。</p>

 <p class="title">足球什么是乌龙球：</p>
<p class="con">由于足球比赛中双方球员拼抢非常激烈，足球在进门之前常常会碰到本方的防守队员。
足球经本方防守队员身体接触后进入球门的情况可以分为两种：如果皮球在进攻队员踢
出后直奔球门的方向，即使有后卫碰到皮球，也应该界定为那个射门球员的入球；而如
果进攻队员射出的球已经偏离球门方向，那么如果防守队员碰到皮球折射入网，就要界定为“乌龙球”。
说直白一点，就是自己人帮对方踢进了球。</p>

 <p class="title">足球区分主队和客队：</p>
<p class="con">一般来说，在左或上的队伍就是主队哦。在下或右的就是客队；</p>
<p class="con">有在比赛时间下面显示一个小的大写字母N就是中立场哦；</p>
<p class="con">中立场系统也是会默认一个为主队一个为客队的呢。</p>

 <p class="title">足球区分上盘和下盘：</p>
<p class="con">让球玩法中让球的一方就是上盘(一般为强队)，受让的一方就是下盘(一般为弱队)；</p>
<p class="con">上盘不一定就在上面，但是在上面的一定是主队。</p>

<p class="con">足球腰斩/中断/待定/延迟：</p>
<p class="con">如果比赛或赛事有中断、腰斩、延迟、待定，并且没有在规定时间内重新开始，所有该场赛事的
投注即被视为无效且取消，除非在个别体育规则里另有指定注明。有相应赛果的订单会相应结算。</p>

 <p class="title">需要注意的是：</p>
<p class="con">【比赛未开始】延期超过12小时，注单会被取消</p>
<p class="con">【已经开始的比赛】中断、腰斩、延迟、待定，超过48小时注单会被取消</p>
<p class="con">注单一旦结算了，72小时后官方有更新赛果，注单都不会改变当初的结算 。</p>

 <p class="title">足球滚球/今日/早盘的区别：</p>
<p class="con">滚球：滚球也叫走地，就是已经开始比赛，正在进行中的比赛哦。</p>
<p class="con">今日：从现在开始到下一个中午12点之前的所有的比赛。</p>
<p class="con">早盘：下一个中午12点以后的所有的比赛。</p>

<p class="con">需要注意的是【下一个中午12点】</p>
<p class="con">①.如果还没过北京时间晚上24点之前，就是正常的下一个中午12点。</p>
<p class="con">②.如果已经过了北京时间晚上24点以后，就是今天中午12点以后。</p>

