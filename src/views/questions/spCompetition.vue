 <p class="title">过关举例说明：</p>

<p class="con">乐购小助手举例说明一个复式串关的计算方式哦：</p>

<p class="con">客户投注本金400元。投注一个3串4.赛事玩法分别为：</p>
 <p class="title">赛事：</p>
<p class="con">A:大阪飞脚VS大阪樱花，大3赔率为1.88</p>
<p class="con">B:札幌冈萨多VS浦和红钻，全场小1/1.5，赔率2.58</p>
<p class="con">C:广州恒大VS上海申花，玩法：恒大让0/0.5，赔率：1.44，下注即时比分为1:0。</p>
<p class="con">完场比分为：大阪飞脚0:3大阪樱花；札幌冈萨多0:1浦和红钻；广州恒大2:1上海申花</p>

 <p class="title">分析：</p>
<p class="con">大阪飞脚VS大阪樱花，这个赛事是盘口打和，赔率按照1计算，
札幌冈萨多VS浦和红钻，这个赛事是赢半的，赔率为【1+2.58】/2=1.79
广州恒大VS上海申花，完场2:1减去下注时的即是比分1:0，投注后实际进球
为1:1，这个时候主队让0/0.5就是输半，输半的赔率为0.5。这个串子客户一
共下注400元，因为3串4一共是4个注单，那么这个复式串关里面单个串关就
是一个注单100元。</p>
<p class="con">所以这个3串4的复式计算方法如下：</p>
<p class="con">100*A*B→100*1*1.79=179</p>
<p class="con">100*A*C→100*1*0.5=50</p>
<p class="con">100*B*C→100*1.79*0.5=89.5</p>
<p class="con">100*A*B*C→100*1*1.79*0.5=89.5</p>
<p class="con">复式3串4注单总盈利为：179+50+89.5+89.5=408，投注本金为400元，</p>
<p class="con">所以这个复式串子实际净盈利8元。</p>

 <p class="title">串关的其他问题：</p>
<p class="con">1: 所有输半的赔率一律为0.5；</p>
<p class="con">2: 所有选项赔率一律包含本金；</p>
<p class="con">3: 每场赛事只能串一次，不看玩法；</p>
<p class="con">4: 单式全输一场赛事整个过关失败；</p>
<p class="con">5: 最低投注2串1，最高10串1013；</p>
<p class="con">6.腰斩/延迟/中断的赛事按照赔率1计算；</p>
<p class="con">7: 赢半赛事赔率计算方式为：【(赔率-1)/2】+1;</p>
<p class="con">8: IM体育最低投注5元;</p>
<p class="con">9: IM体育中 腰斩/延迟/中断/待定的赛事需要等待48小时才能结算串子</p>

