<p class="con">2.如果赛事在下半场取消或中断，所有上半场注单保持有效；</p>
<p class="con">3.预测赛事总比分将大于或小于在盘口指定的大/小盘分数；</p>
<p class="con">温馨提示：O是大球或大于的意思。U是小球或小于的意思。</p>

 <p class="title">篮球单双：</p>
<p class="con">篮球→单双投注：</p>

<p class="con">1.预测赛事的总比分是单数或双数；</p>
<p class="con">2.赛事盘口包括全场、半场或单节投注。</p>
<p class="con">3.0:0也计算双哦。</p>

 <p class="title">篮球三双：</p>
<p class="con">篮球→三双：</p>

<p class="con">投注指定时间内是否有篮板、得分、盖帽、助攻、抢断五项数据
其中的任意三项都达到或者超过两位数，如果超过20那就算大号的数据。</p>

 <p class="title">篮球独赢盘：</p>
<p class="con">篮球→独赢盘：</p>

<p class="con">1.预测哪一支球队将在比赛胜出。盘口提供两支球队为投注选项；</p>
<p class="con">2.赛事盘口包括全场、半场或单节投注。</p>

 <p class="title">篮球得分范围：</p>
<p class="con">篮球→得分范围：</p>

<p class="con">预测比赛的主队和客队的比分总值落在哪个得分范围。
该玩法出现在IM篮球玩法。不计算加时赛。</p>

 <p class="title">篮球特别投注：</p>
<p class="con">篮球→特别投注：</p>

<p class="con">1.预测比分，抢断，篮板，助攻，3分球等；</p>
<p class="con">2.双方球员/球队必须参与比赛，投注才被视为有效；</p>
<p class="con">3.如果一方或双方球员/球队没有参与比赛，所有注单将被视为无效。</p>

 <p class="title">篮球两双/双十：</p>
<p class="con">篮球→双十：</p>

<p class="con">两双也叫双十，是指一场篮球比赛中球员在下面数据中达到两位数：得分、篮板、
助攻、抢断和盖帽。大多数的两双表现都是得分和篮板达到两位数，其次是得分和助攻。</p>

 <p class="title">篮球最先得分球队：</p>
<p class="con">篮球→最先得分球队：</p>

<p class="con">1.预测最先得分的球队；</p>
<p class="con">2.如果赛事在有得分后中断，所有最先得分球队的注单将保持有效；</p>
<p class="con">3.如果赛事在没有球队得分前中断，所有最先得分球队的注单将被视为无效；</p>
<p class="con">4.如果赛事在4节完场时间以及加时赛内没有球队得分，所有最先得分球队的注单将被视为无效。</p>

 <p class="title">篮球最后得分球队：</p>
<p class="con">篮球→最后得分球队：</p>

<p class="con">1.预测最后得分的球队；</p>
<p class="con">2.如果赛事中断， 所有最后得分球队的注单将被视为无效；</p>
<p class="con">3.如果赛事在4节完场时间以及加时赛内没有球队得分，所有最后得分球队的注单将被视为无效。</p>

 <p class="title">梦幻篮球游戏规则：</p>
<p class="con">1.梦幻篮球游戏将从同一天的赛事中任意选择两个球队(且原定不是在同一场赛事比赛的球队)进行投注。</p>
<p class="con">2.梦幻比赛赛果会根据球队真实的比分为准；梦幻比赛的让分数则以球队真实比分来计算。</p>
<p class="con">3.梦幻赛中的两支球队必须在同一天比赛，投注才被视为有效。</p>
<p class="con">4.如果球队的比赛时间和本公司网站显示的时间不同，所有涉及此球队的梦幻赛注单将被视为无效。</p>
<p class="con">6.梦幻赛的举例如下:</p>
<p class="con">a.波士顿凯尔特人101 – 98 芝加哥公牛，洛杉矶湖人118 – 101 奥兰多魔术</p>
<p class="con">b.梦幻赛1: 波士顿凯尔特人vs 洛杉矶湖人</p>
<p class="con">c.梦幻赛赛果:波士顿凯尔特人 101 – 118洛杉矶湖人</p>
<p class="con">d.梦幻赛的结算会根据各球队原定比赛的真实得分为准。</p>

 <p class="title">篮球三项让分投注：</p>
<p class="con">篮球→三项让分投注：</p>

<p class="con">预测比赛结果在加上指定的让分盘口后产生的最终赛果。</p>
<p class="con">该玩法出现在IM篮球玩法。不计算加时赛。</p>

 <p class="title">篮球最高得分赛节：</p>
<p class="con">篮球→最高得分赛节：</p>

<p class="con">预测比赛某个赛节获得最高分不包含加时。</p>
<p class="con">•如果比赛未能完成，所有投注均作废。</p>
<p class="con">该玩法出现在IM篮球玩法</p>

 <p class="title">篮球三项大/小投注：</p>
<p class="con">篮球→三项大/小投注：</p>

<p class="con">预测比赛的比分总值在不包含加时赛的情况下是大于，小于或等于指定分数。如果比赛未能完成，所有投注均作废。</p>
<p class="con">该玩法出现在IM篮球玩法。不计算加时赛。</p>

 <p class="title">篮球净赢分数（赛节）：</p>
<p class="con">篮球→净赢分数(赛节)：</p>

<p class="con">预测主队或客队某个赛节的净赢分数。</p>
<p class="con">该玩法出现在IM篮球玩法。不计算加时赛。</p>

 <p class="title">篮球先获得x分（赛节）：</p>
<p class="con">篮球→先获得X 分(赛节)：</p>

<p class="con">预测哪支球队在某个赛节先获得指定分数。</p>
<p class="con">该玩法出现在IM篮球玩法。不计算加时赛。</p>

 <p class="title">篮球三分球得分（球员）：</p>
<p class="con">篮球→三分球得分(球员)：</p>

<p class="con">预测在上半场，下半场以及全场获得最多三分球的球员。</p>

 <p class="title">篮球三分球得分（球队）：</p>
<p class="con">篮球→三分球得分(球队)：</p>

<p class="con">预测在上半场，下半场以及全场获得最多三分球的球队。</p>

 <p class="title">篮球单节最高得分球队：</p>
<p class="con">篮球→单节最高得分球队：</p>

<p class="con">1.预测单节最高得分的球队；</p>
<p class="con">2.加时赛不计算在内；</p>
<p class="con">3.如果赛事中断，所有单节最高得分球队的注单将被视为无效；</p>
<p class="con">4.如果赛事在4节完场时间以及加时赛内没有球队得分，所有最后单节最高得分球队的注单将被视为无效。</p>

 <p class="title">篮球球队得分最后一位数：</p>
<p class="con">篮球→球队得分 – 最后一位数:</p>

<p class="con">预测主队或客队最终得分的最后一位数字。猜中全赢，猜错全输。</p>

 <p class="title">篮球获胜球队-和局退款（赛节）：</p>
<p class="con">篮球→获胜球队-和局退款 (赛节)：</p>

<p class="con">预测哪支球队胜出某个赛节。如果比赛结果是和局，所有投注均作废。</p>
<p class="con">该玩法出现在IM篮球玩法。不计算加时赛。</p>

