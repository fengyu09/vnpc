 <p class="title">什么时候交换场地：</p>
<p class="con">网球什么时候交换场地：</p>

<p class="con">双方应在每盘的第1、3、5等单数局结束后，
以及每盘结束双方局数之和为单数时，交换场地。</p>

 <p class="title">什么时候抢7和抢十：</p>
<p class="con">网球→什么时候抢7和抢十：</p>

<p class="con">赛制如果是单打就抢7.双打抢10.如果3盘2胜，前面两盘平，
那么第三盘就要抢7或者抢10.谁先抢到加1分。
比如前面两盘比分是 5-3.2-4. 那么最后客队先抢到10
的话，那么就算主队7分 客队8分。</p>

 <p class="title">什么时候交换发球权：</p>
<p class="con">网球什么时候交换发球权：</p>

<p class="con">一局比赛终了，接球员成为发球员，发球成为接球。
以后每局终了。均依次互相交换，直至比赛结束。</p>

 <p class="title">网球的决胜局是如何显示的：</p>
<p class="con">网球→决胜局的显示方式：</p>

<p class="con">决胜局也算作一局比赛。一场比赛的总比分可以只给出总盘数的比分，
(例如3:1 表示胜利者赢3盘，失败者赢1盘，
也可以列出所有局数的比分，但必须先列出获胜者的比分。例如 7-5
6-7(4) 6-4 7-6(6)采用的就是最后一种
计分方式，表示获胜的一方赢得了第一，第三，第四盘的胜利，而第二
和第四盘是靠决胜局生出的，在括号中的数
字表示的输的一方在决胜局中得到的分数，例如在第二盘的决胜局的比
分就是4-7 而第四盘决胜局的比分则是8-6。</p>

