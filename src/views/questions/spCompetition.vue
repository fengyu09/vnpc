<p class="con">有关足球投注的常见其他问题简介</p>

 <p class="title">足球危险球：</p>
<p class="con">危险球的定义</p>

<p class="con">会员在投注足球滚球时，如果赛场中出现以下几种情况，您的投注可能会失败哦：</p>

<p class="con">1.12码罚球、点球；</p>
<p class="con">2.单刀球的产生；</p>
<p class="con">3.黄牌或者红牌的产生；</p>
<p class="con">4.自由球(攻方在守方禁区附近的自由球)；</p>
<p class="con">5.角球 /掷入球(攻方靠近守方禁区的掷入球)；</p>
<p class="con">6.A队向B队禁区附近进攻(或是B队在A队的禁区附近进攻)；</p>
<p class="con">7.门将开球；</p>
<p class="con">8.皮球重新去到中场线；</p>
<p class="con">9.己方球员在自己的后场抢回控球权。</p>

<p class="con">危险球小贴士：无条件危险球有点球、单刀球、红牌；有条件危险球有任意球、角球、射门、危险进攻。</p>

 <p class="title">足球结算金额错误：</p>
<p class="con">您好，如果有注单结算金额和您计算的不一样，亲亲看看是否是滚球投注，是否有赢半和输半的情况哦。</p>

<p class="con">赢半的计算：【(赔率-1)/2+1】 举例：赔率为2.84的赔率的一半为 (2.84-1)/2+1=1.82这是总赔付哦。</p>
<p class="con">输半的计算：本金乘以0.5 举例：本金100，输一半就是 100X0.5=50元。</p>
<p class="con">如果我们有结算错误的注单，我们会在结算后进行二次检测，有发现问题会再次正确结算的呢。所以您放心。</p>

 <p class="title">足球加时赛怎么算：</p>
<p class="con">足球全场90分钟，上下半场各45分钟，加时赛不计算在内，除非盘口有特别说明，
例如伤亡补时将计算在最终比分内，在比分网内以90+形式代表，这种代表伤亡补时。
加时赛不计算在内。例如：全场90分钟结束以后比分为0：0 加时赛比分为2：1，
那么客户投注上结算的依据为0：0。</p>

<p class="con">特别说明：羽毛球、棒球、长曲棍球、垒球所有玩法结算加时赛；</p>
<p class="con">美式足球：第四节投注不包括加时赛。；</p>
<p class="con">篮球：下注全场和下半场计算加时赛(上半场和单节不计算加时赛)</p>
<p class="con">足球点球投注规则：</p>
<p class="con">如果您有投注点球大战的赛事，最后没有进行点球大战，那么是会退本金的哦。</p>
<p class="con">足球点球大战只有让球盘口会计算最后一颗球【突然死亡】。其他的盘口都是</p>
<p class="con">只计算前五轮十个球， 不计算最后一颗球。。而且让球盘口也是要减去即时比分的。</p>

