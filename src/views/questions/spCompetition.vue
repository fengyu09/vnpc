<p class="con">大概等于4的就是包含了本金。</p>
<p class="con">范例：香港赔率0.96，如果下注100,可获得盈利为96，而可损失资金为100。</p>

 <p class="title">欧洲盘：</p>
<p class="con">欧洲盘口是所有其他盘口演算的基础盘口，所以欧洲盘口所有的玩法都是包含本金的。</p>
<p class="con">盘口赔率会显示大于1，下注会先扣除下注金额，赔率是包含本金在内，赢赔率输本金。</p>
<p class="con">范例：欧洲赔率1.96，如果下注100,可获得盈利为96，而可损失资金为100。</p>

 <p class="title">马来盘：</p>
<p class="con">马来盘属于亚洲盘，也叫负水盘，赔率不含本金。</p>

