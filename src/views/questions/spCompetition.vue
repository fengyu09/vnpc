 <p class="title">网球的基本玩法：</p>

 <p class="title">网球让盘：</p>
<p class="con">网球→让盘：</p>

<p class="con">预测哪一位球员在盘口指定的让盘数赢得比赛。</p>

 <p class="title">网球让局：</p>
<p class="con">网球→让局：</p>

<p class="con">预测哪一位球员在盘口指定的让局数在某个时节(例如:全场比赛/第一盘/第二盘)赢得最多局。</p>
<p class="con">决胜局(抢七局)将视为一局。</p>

 <p class="title">网球波胆：</p>
<p class="con">网球→波胆：</p>

<p class="con">预测网球比赛的每盘比分。</p>

 <p class="title">网球独赢盘：</p>
<p class="con">网球→独赢盘：</p>

<p class="con">预测哪一位球员将在比赛胜出。盘口提供两位球员为投注选项。</p>

 <p class="title">网球局获胜者：</p>
<p class="con">网球→局获胜者：</p>

<p class="con">1.预测指定盘的指定局谁将获胜(例如第一盘第一局)；</p>
<p class="con">2.抢七决胜局将视为一局(例如第一盘第十三局)；</p>
<p class="con">3.如果某盘没有进行指定局，则该局所有注单将被取消。
(例如第一盘第八局，但是该盘结束于1-6)；</p>
<p class="con">4.如一名球员退出或被取消资格，任何没有最终获胜者的
局的所有注单将被取消，已结束局的注单将视为有效。</p>

 <p class="title">网球单盘投注：</p>
<p class="con">网球→单盘投注</p>

<p class="con">1.预测单盘最终结果；</p>
<p class="con">2.投注的注单只有在单盘结束后才被视为有效；</p>
<p class="con">3.如果球员在某盘未结束前退出或被取消资格，所有投注此盘的注单将被取消。</p>

 <p class="title">网球总得分-大/小：</p>
<p class="con">网球→总得分：大/小</p>

<p class="con">1.预测比赛中的全场比赛的总得分将大于或小于在盘口指定的大/小盘数。</p>
<p class="con">2.如果比赛中断前已有明确结果并且之后没有任何显著会影响赛事结果的情况，大/小盘注单才会被结算。若遇到任何其他情况，注单将一律被取消。</p>

 <p class="title">网球总局数：大/小：</p>
<p class="con">网球→总局数：大/小</p>

<p class="con">1.决胜局(抢七局)将视为一局。</p>
<p class="con">2.预测比赛中的某个时节(例如:全场比赛/第一盘/第二盘)进行的总局数将大于或小于在盘口指定的大/小盘局数。</p>
<p class="con">3.如果比赛中断前已有明确结果并且之后没有任何显著会影响赛事结果的情况，大/小盘注单才会被结算。若遇到任何其他情况，注单将一律被取消。</p>

