 <p class="title">串关单式和复试的区别：</p>
<p class="con">串关玩法是一种特殊玩法，就是将两场及两场以上的比赛的玩法串到一起进行竞猜，每一个单式串关必须满足最基本的获胜场次才算过关：</p>

 <p class="title">单式串关：</p>
<p class="con">就是单一的串子，所有注单全部串到一个注单上，投注几个注单就是几串1.最低从2串1起一直到最高10串1。IM体育最低投注是5元。最高投注额度依据赛事大小而有所区别。</p>

 <p class="title">复式串关：</p>
<p class="con">复式串关由单式串关组成，所以一个复式串关就是所有单式串关全部加起来就是一个复式串关的盈利金额。</p>

<p class="con">3串4的意思就是下注3个赛事，会有4个注单，因为系统默认的是一个注单的金额，3串4 就是4个注单，
