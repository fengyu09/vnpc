发牌：系统向每位玩家发3张手牌。<br>
比牌：所有玩家亮出手牌，闲家与庄家之间进行比较，闲家之间不进行比牌。
抽水：赢家所赢金额的5%。<br>
<p class="title">结算公式：</p>
A：房间底注<br>
X：庄家抢庄倍数<br>
Y：闲家下注倍数<br>
Z：赢家牌型倍数<br>
庄家胜利所嬴游戏币=A*X*Y*Z-抽水<br>
庄家失败所输游戏币=A*X*Y*Z<br>
闲家胜利所嬴游戏币=A*X*Y*Z-抽水<br>
闲家失败所输游戏币=A*X*Y*Z<br>
防以小博大机制：本局带入多少游戏币，本局最多输赢多少游戏币。<br>
比如您本局带入80，计算时你理论应得200。因为假如您本局输了最多只输80，所以为了公平起见，您本局最多也只能嬴80，而不是200。<br>
特殊情况：如果出现庄家不够输的情况，按照各闲家理论盈利比例结算输赢。<br>
比如庄家有100，一次性输完。A理论应该嬴200，B理论应该嬴200，C理论应该嬴400。由于庄家钱不够输。按照ABC理论盈利占比。<br>
<p class="title">最终得出：</p>
A实际盈利=200/(200+200+400)*100=25<br>
B实际盈利=200/(200+200+400)*100=25<br>
C实际盈利=400/(200+200+400)*100=50<br>

