暗注：不看牌的下注。<br>
单注封顶：每个玩家每次下注的.上限。<br>
回合数封顶：达到轮数.上限，，从最先发话的玩家开始，依次与下家强制发起比牌，此时比牌无需进行下注。<br>
跟注：和上家加入同样的金币。<br>
加注：提高单次押注的金币，加注不能超过单注封顶。<br>
总注：每副牌玩家所有下注之和。<br>
看牌：翻看自己的牌型，看牌后跟注会变成暗牌的2倍。<br>
弃牌：放弃当局牌局。<br>
比牌：第二轮开始才可以比牌，比牌也需要投注，金额与所需跟注金额相同。<br>


