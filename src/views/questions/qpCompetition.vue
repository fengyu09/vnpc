防以小博大说明：<br>
玩家输赢不能超过携带分数
结算时如果玩家输赢超过携带分数，则将输赢改为携带分数
然后分别算出本局输分玩家的总输赢
然后分别算出本局输分玩家的输分总和以及赢分总和，比较两方的
绝对值大小<br>
例如：<br>
同桌ABCD分别带入：1000、500、200、500；<br>
ABCD输赢对应为：-200、-800、900、100；<br>
