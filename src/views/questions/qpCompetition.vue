<p class="title">抢庄牛牛</p>
<p class="title">玩法介绍</p>
定庄：开局玩家开始抢庄家。抢庄倍数最高的玩家为本局庄家，抢庄倍数相同时随机一名玩家坐庄。超时不抢庄，系统自动执行不抢操作。所有人不抢庄，随机一名玩家坐庄。<br>
闲家：定庄后，所有闲家开始下注倍数。携带金额越多，最大可下注倍数越大；超时不下注，系统自动下最小倍数。<br>
开始发牌：系统向每名玩家发五张手牌。<br>
拼牛：玩家拿到五张手牌后即开始拼牛。<br>
比牌：拼牛完成后，所有玩家亮出手牌。闲家只与庄家比，闲家之间不互相比。<br>
防以小博大机制：本局带入多少金额，本局最多就输赢多少金额。比如您本局带入50，计算时你理论上应得100。但假如您本局输了最多只输50，为了公平起见，您本局最多也只能赢50，而不是100。<br>
抽水：赢家所赢金额的5%<br>
特殊情况：如果出现庄家不够赔的情况，按照各闲家理论盈利比例结算输赢。比如庄家有100，一次性输完，A理论应该赢100，B理论应该赢150，C理论应该赢250。由于庄家钱不够赔，按照A、B、C理论盈利占比，最终得出：<br>
A实际盈利=100/(100+150+250)*100=20<br>
B实际盈利=150/(100+150+250)*100=30<br>
C实际盈利=250/(100+150+250)*100=50<br>
注：<br>
有极低概率会出现庄家不够赔且陪付金额无法整除，零头将随
机分配给一位赢家。<br>

