2.3 特殊玩法---小玛丽<br>
押线中连续3个或3个以上WILD时进入小玛丽游戏。<br>

外盘：滚动图案区（7种水果类型共20个水果图案+4个）<br>

奖励表：7个中奖图案倍率。<br>
西瓜x200；葡萄x100；柠檬x70；樱桃x50；香蕉x20；橙子x10；菠萝x5。<br>

内盘：4个中奖图案公示区，每轮启动时刷新。<br>
图案可重复，当刷出3或4个相同图案时，额外获得总注的20倍或500倍奖励！<br>

每次启动时，内盘4个图案随机变化。当外盘停止时停留的图案与内盘图案有相同时，视为中奖。注意：中奖图案内盘有多个时（N），奖励会相应增加。<br>
中奖金额=（总注*中奖图案倍率）*N<br>
举例：<br>
内盘结果：菠萝 西瓜 菠萝 菠萝   总注9    外盘结果：菠萝<br>
中奖=（9*5）*3=135<br>

内盘结果：菠萝 菠萝 菠萝 西瓜   总注9    外盘结果：菠萝<br>
中奖=（9*5）*3+9*20=315<br>

内盘结果：菠萝 菠萝 菠萝 菠萝    总注9    外盘结果：菠萝<br>
中奖=（9*5）*4+9*500=4680<br>

当转盘停止时停留的图案与公示区图案不相同时，不中奖。系统自动启动下一轮！<br>
当转盘停止时停留的图案是炸弹时，退出小玛丽！注意，第1轮永不为炸弹。<br>

