比牌：所有闲家都与庄家进行比牌，闲家之间不进行比牌<br>
防以小博大机制：<br>
本局带入多少游戏币，本局最多输赢多少游戏币<br>
例如：本局带入80，结算时理论应得200，但假如本局输了最多只输80，为公平起见，本局最多也只能赢80，而不是200。<br>
如果出现庄家不够输的情况，按照闲家理论盈利比例结算输赢例如：庄家有100，一次性输完，A理论应该赢200，B理论应该赢200， C理论应该赢400， 由于庄家钱不够输， 按照ABC，最终得出：<br>
A实际盈利=200/(200+200+400)*100=25<br>
B实际盈利=200/(200+200+400)*100=25<br>
C实际盈利=400/(200+200+400)*100=50<br>
抽水：赢家所赢金额的5%<br>

