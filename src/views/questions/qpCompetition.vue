基本牌型大小：爆玖>炸弹>三公>9点>8点>7点>6点>5点>4点>3点>2点>1点>0点
三公牌型比较：如果多家牌型都是三公，则先比较玩家最大的那张公仔牌，如果公仔牌相同，则比较玩家最大公仔牌的花色<br>
点数牌比较：如果多个玩家都是点数相同的牌，那么先比较玩家的公仔牌数量谁的公仔牌数量多谁胜，如果公仔牌的数量-致，则比较最大的那张单牌的点数大小，如果点数相同则比较花色

