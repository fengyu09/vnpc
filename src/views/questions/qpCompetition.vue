<p class="title">斗地主</p>
<p class="title">玩法介绍</p>
游戏由三个人玩一副牌，地主是一方，其余两家为另一方，双方对战，先出完的一方胜利。<br>
发牌：一副牌54张，每人17张，留3张做底牌，在确定地主之前玩家不能看底牌。<br>
叫牌：系统随机选择一人开始叫牌，按逆时针顺序轮流进行。叫牌时可以选择1分、2分、3分和不叫；叫分持续到一个人叫3分或者另外两个人都不叫分为止。如果都“不叫”则重新发牌，重新叫牌。叫牌结束后，地主玩家获得三张底牌。出牌：由地主先出牌，然后按逆时针顺序依次出牌，轮到玩家跟牌时玩家可选择不出或者按规则出牌。直至某一方牌先出完
结束此局。<br>
1.继续游戏按钮没出来前严禁玩家退出。<br>
2.玩家强制退出游戏，不会退还已下底分，并且按正常流程进行收益结算。<br>

<p class="title">牌型计算</p>
王炸：即双王(双鬼)，最大牌。<br>
炸弹：4张相同点数牌。<br>
单牌：单张牌，比如3。<br>
对子：2张点塑相同的牌。<br>
三张：3张点数相同的牌。<br>
三带一：3张点数相同牌+1张单牌。例如3张3+单4.<br>
三代二：3张点数相同的牌+1对牌。例如3张3+对4。<br>
单顺子：五张或更多连续的牌。例如单3+单4+单5+单6+单7；不包括2点和双王。<br>
双顺子：三张或更多连续的对牌。例如对3+对4+对5；不包括2点和双王。<br>
三顺子：2个或更多连续的三张牌。例如三张3+三张4+三张5.不包括2点和双王。也叫飞机不带翅膀。<br>
飞机带翅膀：三顺+同数量的单牌(或同数量的对牌)<br>
四带二四张牌+任意2张单牌(或任意2对牌)；例如四张5+单3+单4或四张4+对5+对6。<br>
<p class="title">牌型大小</p>
1：王炸最大，可以打任意其他的牌。<br>
2：炸弹比王炸小，比其他牌大。都是炸弹时按牌的点数比大小。<br>
3：除王炸和炸弹外，其他牌必须要牌型相同且总张数相同才能比大小。<br>
4：单牌按分值比大小，依次是：大王>小王>2>A>K>Q>J>10>9>8>7>6>5>4>3，不分花色。<br>
5：对牌、三张牌都按分值比大小。<br>
6：顺牌按最大的一张牌的分值来比大小。<br>
7：飞机带翅膀和四带二按其中的三顺额四张部分来比，带的牌不影响大小。<br>

<p class="title">牌型倍数</p>
炸弹：每出一个炸弹，倍数×2.<br>
王炸：倍数×2.<br>
春天：农民一张牌没出，地主就赢了，出现这种情况时倍数×2。<br>
反春天：地主只出了一手牌，农民就赢了，此时倍数×2。<br>

<p class="title">结算</p>
X：底分<br>
Y：倍数<br>
地主胜利所赢金币=农民A失败所输金币+农民B失败所输金币-抽水。<br>
地主失败所输金币=农民A胜利所赢金币+农民B胜利所赢金币+抽水。<br>
农民胜利所赢金币=X*Y-抽水；<br>
农民失败所输金币=X*Y；<br>
抽水：赢家所赢金额的5%。<br>
防以小博大机制：<br>
情况1：农民胜利<br>
农民A所赢金币=(农民A携带、X*Y、地主携带金币/2)中取最小值-抽水农民B所赢金币=(农民B携带、X*Y、地主携带金币/2)中取最小值-抽水<br>
情况2：地主胜利<br>
农民A所输金币=(农民A携带、X*Y、地主携带金币/2)中取最小值。<br>
农民B所输金币=(农民B携带、X*Y、地主携带金币/2)中取最小值地主所赢金币=农民A所输金币+农民B所输金币-抽水。<br>
倍数限制：为避免产生过大的输赢倍数，炸弹和王炸产生的倍数不得超过牌桌最大倍数限制。比如某房间最大倍数限制是8倍，农民A连出了3个炸弹，此时倍数达到8倍限制。再出炸弹或王炸都不再计算倍数。<br>
