<p class="title">诈金牛</p>
<p class="title">玩法介绍</p>
由一副牌(52张无大小王)3人~5人同时进行游戏。<br>
发牌之前大家先投入对应的金币作为底分，每人依次发5张牌，默认牌面向下，为暗牌。随机一家先进行发话，从他下家开始逆时针轮流操作。轮到玩家操作时，玩家根据条件和判断形势可以进行跟注、加注、看牌、弃牌、比牌操作进行游戏，对局结束后获胜的玩家将获得本桌.上对应的金币。<br>
抽水：赢家所赢金额的5%<br>
孤注一掷：“孤注一掷”时自动从下家开始与所有剩余玩家轮流比牌，此情况下获胜，返还其他玩家多余的投注金币并赢得桌面剩余金币，其他玩家多余的投注金币=剩余玩家本轮投注金币*(玩家自己应该投注金币-玩家自己实际投注金币)/玩家自己应该投注金币。如玩家比牌时应该投注10，但自己投了5，他进行孤注一-掷获胜，剩余玩家本轮投注了20，则需要给剩余玩家返还20*(10-5)/10=10。非“孤注一掷”情况下获胜，可以赢得桌面所有金币。<br>
防超时弃牌：默认勾选。勾选后，看牌情况下，在不可比牌阶段超时未操作且牌型为牛牛及牛牛以上时，会自动跟注；其他情况则执行弃牌。在可比牌阶段，玩家已看牌情况下超时未操作，若玩家是牛牛及牛牛以上的牌型，系统会自动进行比牌操作；若玩家是牛牛以下牌型(不包括牛牛)，则会执行弃牌操作，未看牌勾选按钮超时将自动弃牌。<br>

