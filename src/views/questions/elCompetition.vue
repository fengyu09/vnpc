<p class="title">电竞赛事的有效性：</p><br>
赛事的有效性：<br>
1.如果主播未进入到对应游戏的正式玩法画面（赛前准备除外）就退出本局比赛，
或同局内的其他玩家退出导致游戏无法进行的情况，将不会记为一次有效的比赛，
对应的比赛次序会顺延至下一个有效的比赛。<br>
2.如果主播未进行对应游戏的比赛而进行了其他游戏的比赛，将不会记为一次有效
的比赛，对应的比赛次序会顺延至下一个有效的比赛。<br>
3.在英雄联盟主播游戏进行四分钟内，因一方选手掉线，并在规定时间内完成并通过
重开（remake）投票，则该局比赛将不会记为一次有效的比赛，对应的比赛次序会
顺延至下一个有效的比赛。<br>
