<p class="title">电竞游戏让分盘：</p>
电竞让分盘投注：<br>

1.电子竞技比赛的让分可以是轮/赛局或根据比赛不同选择其他的计量方式。<br>
2.让分的目的是让体育赛事双方更加对等，让博彩更加有趣。在电子竞技比赛博彩中，具体做法是向实力较差的一方派送一定的领先比分/胜出局。<br>
3.假如选手A让选手B，1.5分，如果选手A多赢了两张或以上地图，则投注选手A的玩家胜出，投注选手B的玩家落败。如果选手A只多赢了一张地图或选手B获胜，则投注选手B的玩家胜出，投注选手A的玩家落败。<br>
4.总分盘投注：电子竞技比赛的总分盘可以是轮/局赛况或是基于比赛的其他计分投注。总分仅指总得分。<br>
5.举例：SC2赛事盘口全场局数高于或低于2.5，若玩家以2-0获胜，所有低于2.5的注码为胜，高过2.5则为输。若玩家以2-1获胜，所有高过2.5的注码为胜，所有低于2.5的注码则为输。<br>

<p class="title">电竞游戏大小盘：</p><br>
电竞大小盘投注：<br>
1.大小盘的意思是预测比赛中特定项目的总数会大于或者小于指定的数值来分出胜负。<br>
2.分钟数：在地图比赛时间上提供的总分盘（以分钟计）。<br>
如果地图比赛时间短于提供的总分盘，那么“小盘”上的投注将获胜，“大盘”上的投注将输掉。
如果地图比赛时间长于提供的总分盘，那么“大盘”上的投注将获胜，“小盘”上的投注将输掉。
如果地图比赛时间的分钟数和提供的总分盘相同，“大盘”上的投注将获胜，“小盘”上的投注将输掉。
3.所有和结束时间有关的玩法都是以最后弹出失败或者胜利的页面框的时间为准，不以失败时间为准。<br>
4.例子：Dota2的分钟数特别投注上提供的总分盘为36。
如果地图在36:02结束，那么“大盘”上的投注将获胜，“小盘”上的投注将输掉。
如果地图在35:45结束，那么“小盘”上的投注将获胜，“大盘”上的投注将输掉。
如果地图正好在36:00结束，“大盘”上的投注将获胜，“小盘”上的投注将输掉。<br>
5.在 Dota2和英雄联盟的比赛中，会根据完赛后的赛后统计面板（下图例）所显示的结果派彩。
